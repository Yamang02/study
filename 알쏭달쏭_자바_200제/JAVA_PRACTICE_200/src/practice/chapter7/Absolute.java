package practice.chapter7;

public class Absolute {
	public int absolute(int x) {
		return x > 0 ? x : -x;
	}

	public long absolute(long x) {
		return x > 0 ? x : -x;
	}

	public float absolute(float x) {
		return x > 0 ? x : -x;
	}

	public double absolute(double x) {
		return x > 0 ? x : -x;
	}
}
